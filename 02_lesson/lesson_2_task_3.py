"""
Этот модуль содержит функцию для вычисления площади квадрата
и округления результата вверх, если сторона квадрата не является целым числом.
"""

import math

# Функция для вычисления площади квадрата
def square(side_length):
    """
    Функция вычисляет площадь квадрата.

    :param side_length: длина стороны квадрата
    :return: площадь квадрата, округленная вверх, если сторона не целая
    """
    # Вычисляем площадь
    area = side_length * side_length
    # Округляем результат вверх, если площадь не целая
    return math.ceil(area)

# Пример использования функции
side_length_value = 4.5  # Введите любую сторону квадрата
result = square(side_length_value)
print(f"Площадь квадрата со стороной {side_length_value}: {result}")
